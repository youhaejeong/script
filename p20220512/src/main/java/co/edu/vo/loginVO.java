package co.edu.vo;

public class loginVO {
	private String email;
	private String pw;
	
	
	
	
	
	@Override
	public String toString() {
		return "loginVO [email=" + email + ", pw=" + pw + "]";
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	
	
	
	
	
	
	

}
